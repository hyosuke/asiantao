SELECT
t1.USER_ID AS USER_ID
,t2.USER_NM AS USER_NM
,t1.SELECT_MONTH AS SELECT_MONTH
,t1.FIRST_FG AS FIRST_FG
,t1.DAY AS DAY
,t1.SCHEDULE_DV AS SCHEDULE_DV
,t1.BIKOU AS BIKOU
FROM SCHEDULE_DETAIL t1
INNER JOIN M_USER t2
ON t1.USER_ID = t2.USER_ID
WHERE
t1.SELECT_MONTH = /*selectMonth*/'201512'
AND
t1.DAY = /*day*/'1'
