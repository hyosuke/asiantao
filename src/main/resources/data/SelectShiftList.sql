SELECT
t1.DAY AS DAY
,t2.DV_NAME AS SCHEDULE_DV_NM
,t4.USER_NM AS USER_NM
,t3.DV_NAME AS POZISHON_DV_NM
,t1.KAKUTEI_BIKOU AS BIKOU
FROM SCHEDULE_DETAIL t1
INNER JOIN S_DV t2
ON t1.KAKUTEI_SCHEDULE_DV = t2.DV_VALUE
AND t2.DV = 'KAKUTEI_SCHEDULE_DV'
INNER JOIN S_DV t3
ON t1.KAKUTEI_POZISHON_DV = t3.DV_VALUE
AND t3.DV = 'KAKUTEI_POZISHON_DV'
INNER JOIN M_USER t4
ON t1.USER_ID = t4.USER_ID
WHERE
t1.SELECT_MONTH = /*selectMonth*/'12'
AND
t1.FIRST_FG = /*firstFg*/'1'
AND
t1.DAY = /*day*/'1'
AND
t1.KAKUTEI_SCHEDULE_DV IS NOT NULL
ORDER BY t1.DAY asc,t1.KAKUTEI_SCHEDULE_DV asc